/**
 * Returns the current album id that's set by the server.
 * @return {string|null}
 */
export function getCurrentArtistId() {
    return window.currentArtistId;
}

/**
 * Returns the current album id that's set by the server.
 * @return {string|null}
 */
export function getCurrentAlbumId() {
    return window.currentAlbumId;
}
